void PrintMatrixFancy(int[,] matrix)
{
  for (int i = 0; i < matrix.GetLength(0); i++)
  {
    for (int j = 0; j < matrix.GetLength(1); j++)
    {
      System.Console.Write("{0, 2}", matrix[i, j] == 1 ? "#" : ".");
    }
    System.Console.WriteLine();
  }
}

bool ValidateCoord(int n, int length)
{

  return (n >= 0 && n < length);
}

void FillImage(int[,] image, int x, int y)
{
  //System.Console.WriteLine($"x = {x} y = {y}");
  if (ValidateCoord(x, image.GetLength(0)) && ValidateCoord(y, image.GetLength(1)))
  {
    // System.Console.WriteLine($"x = {x} y = {y}");
    if (image[x, y] == 0)
    {
      image[x, y]++;
      FillImage(image, x, y - 1); //left
      FillImage(image, x - 1, y); //top
      FillImage(image, x, y + 1); //right
      FillImage(image, x + 1, y); //down
    }
  }
}

